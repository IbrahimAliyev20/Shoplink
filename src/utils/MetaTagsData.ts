// Helper function to get meta tags by index
export const getMetaTagsByIndex = (index: number): MetaTagData => {
  return metaTagsData[index] || metaTagsData[0];
};

// Helper function to get meta tags by page name
export const getMetaTagsByPageName = (pageName: string): MetaTagData => {
  const page = metaTagsData.find(meta => 
    meta.title.toLowerCase().includes(pageName.toLowerCase())
  );
  return page || metaTagsData[0];
};

